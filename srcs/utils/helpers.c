#include "cub3d.h"

char    *to_str(char c, t_gc *gc)
{
    char    *s;

    s = gc_strainer(gc, malloc(sizeof(char) * 2));
    if (!s)
        return (NULL);
    s[0] = c;
    s[1] = '\0';
    return (s);
}
